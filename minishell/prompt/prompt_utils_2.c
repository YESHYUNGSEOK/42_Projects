#include "../include/prompt.h"

void	sig_int_handler(int signo)
{
	if (signo == SIGINT)
	{
		printf("\n");
		rl_on_new_line();
		rl_replace_line("", 1);
		rl_redisplay();
		add_set_env_to_list(g_global->g_envl, "?=1");
	}
}

void	init_sighandler(void)
{
	rl_catch_signals = 0;
	signal(SIGINT, sig_int_handler);
	signal(SIGQUIT, SIG_IGN);
}

