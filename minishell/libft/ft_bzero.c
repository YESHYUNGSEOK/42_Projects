#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		idx;
	char		*start;

	if (n == 0)
		return ;
	idx = 0;
	start = s;
	while (idx < n)
	{
		start[idx] = 0;
		idx++;
	}
}
